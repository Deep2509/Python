# Programme to find square using lambda function

sq=lambda x:x*x

x=int(input("Please enter a number: "))
print("Square of the number would be: ",sq(x))


#Programme to find square root of a number using lambda function

sqroot=lambda x:x**(1/2)

y=int(input("Enter a number to get its square root: "))
print("Square root of a number would be: ",sqroot(y))
